public class Math {

    public int Add(int a, int b)
    {
       return a + b;
    }

    public int Sub(int a, int b)
    {
        return a - b;
    }

    public int Mult(int a, int b)
    {
        return a * b;
    }

    public int Div(int a, int b)
    {
        return a / b;
    }

    public int Square(int a)
    {
        return a * a;
    }

    public int Cube(int a)
    {
       return a * a * a;
    }

}
